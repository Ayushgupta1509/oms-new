import React, { Component } from 'react'
import GoogleMap from 'google-map-react'
import Marker from './Marker'
import Polyline from './Polyline'

class Maps extends Component {
  constructor (props) {
    super(props)

    this.state = {
      mapsLoaded: false,
      map: null,
      maps: null,
      posts:[]
    }
  }

componentDidMount(){
  fetch('http://meters.siotel.in:8005/geomap/site/').then(resp=>resp.json())
  .then(resp=>this.setState({posts:resp}))
}

  onMapLoaded (map, maps) {
    this.fitBounds(map, maps)

    this.setState({
      ...this.state,
      mapsLoaded: true,
      map: map,
      maps: maps
    })
  }

  fitBounds (map, maps) {
    var bounds = new maps.LatLngBounds()
    for (let marker of this.props.markers) {
      bounds.extend(
        new maps.LatLng(marker.lat, marker.lng)
      )
    }
    map.fitBounds(bounds)
  }

  afterMapLoadChanges () {
    return (
      <div style={{display: 'none'}}>
        <Polyline
          map={this.state.map}
          maps={this.state.maps}
          markers={this.props.markers} />
      </div>
    )
  }

  render () {
    return (
<>

<GoogleMap
        bootstrapURLKeys={{key: 'AIzaSyBZe6zfgKt1_ECwEnLXDcLGIEPeJ1uWKlI'}}
        defaultCenter={this.props.center}
        defaultZoom={this.props.zoom}
        onGoogleApiLoaded={({map, maps}) => this.onMapLoaded(map, maps)}>
          {this.state.posts.map(post=>
         
   <Marker text={post.site_id} lat={post.lat} lng={post.long} />
       )} 

        {this.state.mapsLoaded ? this.afterMapLoadChanges() : ''}
      </GoogleMap>
      
</>
      
    )
  }
}

